    void  HienDanhSachDiem()
    {
       
       List<Bangdiem> list = new BangDiemDAO().getListByMSSV("1842002");
       
       if(list!=null)
       {
           for(Bangdiem item : list)
           {
               cmbMonHoc.addItem(item.getMaBangDiem().split("-")[1].toString());
           }
       }
       
        
        
        
        
    }
    
    
