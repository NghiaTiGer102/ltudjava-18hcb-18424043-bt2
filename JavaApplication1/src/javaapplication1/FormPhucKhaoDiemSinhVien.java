    private void btnXacNhanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXacNhanActionPerformed
        
    // TODO add your handling code here:
        Phuckhao pk = new PhucKhaoDiemDAO().TonTai();
        if(pk.getId()!=null)
        {
            String diemLua ="";
            if(rdGK.isSelected())
            {
                diemLua = rdGK.getText();
            }
            else if(rdCK.isSelected())
            {
                diemLua = rdCK.getText();
            }
            else if(rdDK.isSelected())
            {
                diemLua = rdDK.getText();
            }
            else
            {
                diemLua = rdALL.getText();
            }
            pk.getId().setMssv(lbMSSV.getText());
            
               
            Phuckhao pkthem = new Phuckhao(pk.getId(), cmbMonHoc.getSelectedItem().toString() , "Chưa xem", lbHoTen.getText(),diemLua , txtDiemMongMuon.getText(), txtLyDo.getText());
           new      PhucKhaoDiemDAO().ThemChoSinhVien(pkthem);
            JOptionPane.showMessageDialog(null, "Gừi phúc khảo thành  công","Thông báo" , JOptionPane.INFORMATION_MESSAGE);
        
            
           }
          else
            {
              JOptionPane.showMessageDialog(null, "Chưa đến ngày phúc khảo xin kiểm tra lai","Thông báo" , JOptionPane.INFORMATION_MESSAGE);
          
                
            }
            
        
        
    }//GEN-LAST:event_btnXacNhanActionPerformed

