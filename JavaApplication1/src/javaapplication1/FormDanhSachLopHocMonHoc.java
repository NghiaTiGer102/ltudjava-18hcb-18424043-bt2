     void LoadDuLieuSinHVien(String[] columnNames,String lophoc)
    {
         List<Lopmonhoc> list = new LopMonHocDAO().getByID(cmdLopMonHoc.getSelectedItem().toString());
        
        String[][] listDataSinhVien = new String[list.size()][5];
        for(int i =0;i<list.size();i++)
        {
              listDataSinhVien[i][0] = (i+1) + "";
              listDataSinhVien[i][1] =  list.get(i).getMssv();
              listDataSinhVien[i][2] =  list.get(i).getHoTen();
               
              String gt="Nữ";
              if(list.get(i).getGioiTinh())
              {
                  gt="Nam";
              }
              listDataSinhVien[i][3] = gt;
              listDataSinhVien[i][4] =  list.get(i).getCmnd();
        }
        
        
        
        TableModel tablemodel = new DefaultTableModel(listDataSinhVien, columnNames);
        tbSinhVienMonHoc.setModel(tablemodel);
        
    
    }
    void  HienThiDanhSachLopLenCombobox()
    {
       
        Lopkhoabieu lhocdao = new Lopkhoabieu();
        List<Lopkhoabieu> listlophoc =  new LopKhoaBieuDAO().getALL();
        if(listlophoc==null)
        {
            return;
        }
        for(Lopkhoabieu item :listlophoc)
        {
             cmdLopMonHoc.addItem(item.getMaLopKhoaBieu().toString());
        }
      
    }
    
    
