    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        if(txtCMND.getText().length()==0||txtMSSV.getText().length()==0||txtHoTen.getText().length()==0)
        {
             JOptionPane.showMessageDialog(null, "Dữ liệu không hợp lệ xin kiểm tra lại","Thông báo" , JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        
          Boolean gioiTinh=false;
         if(rdbNam.isSelected())
         {
             gioiTinh =true;
         }
          
         LopMonHocDAO lopmhdao = new LopMonHocDAO();
        Lopmonhoc lopmh = new Lopmonhoc(cmdLopMonHoc.getSelectedItem().toString()+txtMSSV.getText(), txtMSSV.getText(),txtHoTen.getText(), gioiTinh, txtCMND.getText(),cmdLopMonHoc.getSelectedItem().toString());
       lopmhdao.ThemLopMonHoc(lopmh);
             
             
             
        if(cmdLopMonHoc.getItemCount()>0)
        {
             LoadDuLieuSinHVien(columnNames, cmdLopMonHoc.getSelectedItem().toString());
        }
       
        
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        
        int check = tbSinhVienMonHoc.getSelectedRowCount();
        
        if(check==0)
        {
            return;
        }
        
        try {
            Lopmonhoc svl = new LopMonHocDAO().getByMSSV(tbSinhVienMonHoc.getValueAt(tbSinhVienMonHoc.getSelectedRow(), 1).toString());
            if(svl!=null)
            {
                new LopMonHocDAO().XoaLopMonHoc(svl);
            }
        } catch (Exception e) {
        }
        
        
       
       if(cmdLopMonHoc.getItemCount()>0)
        {
             LoadDuLieuSinHVien(columnNames, cmdLopMonHoc.getSelectedItem().toString());
        }
            
            
        
         
        
    }//GEN-LAST:event_btnXoaActionPerformed

