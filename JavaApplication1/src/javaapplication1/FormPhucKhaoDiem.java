    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        // TODO add your handling code here:
           int check = tblDanhSachPhucKhao.getSelectedRowCount();
        
        if(check==0)
        {
            JOptionPane.showMessageDialog(null,"Chưa chọn sinh viên phúc khảo","Thông báo",JOptionPane.INFORMATION_MESSAGE);
            return;
        }
          try {
             String mapk =  tblDanhSachPhucKhao.getValueAt(tblDanhSachPhucKhao.getSelectedRow(), 1).toString();
             String ngaybd = tblDanhSachPhucKhao.getValueAt(tblDanhSachPhucKhao.getSelectedRow(), 2).toString();
           String ngakt =  tblDanhSachPhucKhao.getValueAt(tblDanhSachPhucKhao.getSelectedRow(), 3).toString();
          String mssv = tblDanhSachPhucKhao.getValueAt(tblDanhSachPhucKhao.getSelectedRow(), 4).toString();
          
          SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                        Date datebd = sdf.parse(ngaybd);
                        Date datekt = sdf.parse(ngakt);
             
            Phuckhao pk = new PhucKhaoDiemDAO().TimKiem(mapk,datebd,datekt,mssv);
            if(pk.getId()!=null)
            {
                pk.setTrangThai(cmbTrangThai.getSelectedItem().toString());
                new PhucKhaoDiemDAO().Sua(pk);
            }
        } catch (Exception e) {
        }
          JOptionPane.showMessageDialog(null,"Cập  nhật thành công","Thông báo",JOptionPane.INFORMATION_MESSAGE);
        LoadSinhVienPhucKhao();
    }//GEN-LAST:event_btnCapNhatActionPerformed

