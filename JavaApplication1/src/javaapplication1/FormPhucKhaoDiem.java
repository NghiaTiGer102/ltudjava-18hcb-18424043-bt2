    void HienThiTable(List<Phuckhao> list)
    {
        if(list==null)
        {
                  
        TableModel tablemodel = new DefaultTableModel(null, columnNames);
        tblDanhSachPhucKhao.setModel(tablemodel);
        }
String[][] listDataSinhVien = new String[list.size()][11];
    int dem=0;
        for(int i =list.size()-1;i>=0;i--)
        {
            if( list.get(i).getMonPhucKhao()==null)
            {
                continue;
            }
              listDataSinhVien[dem][0] = (dem+1) + "";
              listDataSinhVien[dem][1] =  list.get(i).getId().getMaPhucKhao();
              listDataSinhVien[dem][2] =  list.get(i).getId().getNgayBd().toString();
             
                listDataSinhVien[dem][3] =  list.get(i).getId().getNgayKt().toString();
                        listDataSinhVien[dem][4] =   list.get(i).getId().getMssv();
                 listDataSinhVien[dem][5] =  list.get(i).getHoTen();
              listDataSinhVien[dem][6] =   list.get(i).getMonPhucKhao();
       
                            listDataSinhVien[dem][7] =  list.get(i).getCotDiemPhucKhao();
                            listDataSinhVien[dem][8] =  list.get(i).getDiemMongMuon();
                            listDataSinhVien[dem][9] =  list.get(i).getLyDo();
                            listDataSinhVien[dem][10] =  list.get(i).getTrangThai();  
                            dem++;
        }
        
        
        
        TableModel tablemodel = new DefaultTableModel(listDataSinhVien, columnNames);
        tblDanhSachPhucKhao.setModel(tablemodel);
    }
    
    void LoadSinhVienPhucKhao()
    {
         List<Phuckhao> listpk = new PhucKhaoDiemDAO().getALL();
         
         HienThiTable(listpk);
         
    }
    
